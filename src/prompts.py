def load_school_info():
    data_dir = Path("data")
    if not data_dir.exists():
        return "暂无校园知识库，请在项目根目录创建data文件夹并放入md文档"
    md_files = sorted(data_dir.glob("*.md"))
    if not md_files:
        return "data文件夹内暂无知识库文件"
    return "\n\n".join(
        f"==== {f.name} ====\n{f.read_text(encoding='utf-8')}"
        for f in md_files
    )

